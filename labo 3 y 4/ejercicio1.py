# Texto inicial
texto = "python"

# longitud del texto
longitud_texto = len(texto)

# Convertimos a float
longitud_float = float(longitud_texto)

# Convertir a string nuevamente
longitud_string = str(longitud_float)

# Imprimimos los resultados
print("Longitud del texto:", longitud_texto)
print("Longitud como float:", longitud_float)
print("Longitud como string:", longitud_string)
