# Operaciones matematicas
suma = 8 + 6
resta = 8 - 6
multiplicacion = 8 * 6
division = 8 / 6
modulo = 8 % 6
division_entera = 8 // 6
potencia = 8 ** 6

# Utilizando f-strings 
print(f"8 + 6 = {suma}")
print(f"8 - 6 = {resta}")
print(f"8 * 6 = {multiplicacion}")
print(f"8 / 6 = {division:.2f}")
print(f"8 % 6 = {modulo}")
print(f"8 // 6 = {division_entera}")
print(f"8 ** 6 = {potencia}")
